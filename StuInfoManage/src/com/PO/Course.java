package com.PO;

public class Course {

	private String coId;
	private String coName;
	private String coNature;
	private String coScore;
	
	public String getCoId() {
		return coId;
	}
	public void setCoId(String coId) {
		this.coId = coId;
	}
	public String getCoName() {
		return coName;
	}
	public void setCoName(String coName) {
		this.coName = coName;
	}
	public String getCoNature() {
		return coNature;
	}
	public void setCoNature(String coNature) {
		this.coNature = coNature;
	}
	public String getCoScore() {
		return coScore;
	}
	public void setCoScore(String coScore) {
		this.coScore = coScore;
	}
	
}
